-- TRIGGER1(매도,매수시 계좌,유가증권 평가금액 변동)
CREATE OR REPLACE TRIGGER TRADETRG
AFTER INSERT ON TRADE_BOARD
FOR EACH ROW 
BEGIN 
	IF :NEW.STATUS = '매도'
	THEN 
		UPDATE MEMBER
		SET ACCOUNT = ACCOUNT + (:NEW.PRICE*:NEW.HOLDING)
		WHERE ID = :NEW.ID;
	END IF;
	IF :NEW.STATUS = '매수'
	THEN 
		UPDATE MEMBER
		SET ACCOUNT = ACCOUNT - (:NEW.PRICE*:NEW.HOLDING)
		WHERE ID = :NEW.ID;
	END IF;
END;



